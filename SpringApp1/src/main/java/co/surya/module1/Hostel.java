package co.surya.module1;

public class Hostel {
	private String Hostelname;
	private String city;
	public Hostel(){}
	public String getHostelname() {
		return Hostelname;
	}
	public void setHostelname(String hostelname) {
		Hostelname = hostelname;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
	

}
